# Run NTP phase
run_ntp_phase() {
    print_phase_header "10" "Time Synchronization"

    # Check skip flag
    if [[ "${SKIP_NTP:-false}" == "true" ]]; then
        log_info "Skipping NTP configuration (--skip-ntp)"
        return 0
    fi

    print_section "NTP Configuration"

    configure_ntp || return 1
    enable_ntp_sync || return 1

    print_section "Verification"

    verify_time_sync

    if [[ "${VERBOSE:-false}" == "true" ]]; then
        display_time_info
    fi

    log_success "Time synchronization configured"
    return 0
}

# Run Netbird phase
run_netbird_phase() {
    print_phase_header "11" "Netbird VPN"

    # Check skip flag
    if [[ "${SKIP_NETBIRD:-false}" == "true" ]]; then
        log_info "Skipping Netbird configuration (--skip-netbird)"
        return 0
    fi

    # Check if setup key provided via CLI
    if [[ -z "${NETBIRD_KEY:-}" ]]; then
        log_info "No Netbird setup key provided via CLI"

        if [[ "${NON_INTERACTIVE:-false}" != "true" ]]; then
            # Use the new interactive prompt
            if ! prompt_netbird_config; then
                log_info "Skipping Netbird configuration"
                return 0
            fi
        else
            log_info "Skipping Netbird (no key in non-interactive mode)"
            return 0
        fi
    fi

    if [[ -z "${NETBIRD_KEY:-}" ]]; then
        log_info "No setup key provided, skipping Netbird"
        return 0
    fi

    print_section "Installation"

    if ! check_netbird_installed; then
        install_netbird || return 1
    else
        log_success "Netbird already installed"
    fi

    print_section "Configuration"

    echo ""
    echo -e "  ${YELLOW}WARNING: Netbird connection may briefly interrupt SSH access.${NC}"
    echo ""

    if [[ "${NON_INTERACTIVE:-false}" != "true" ]]; then
        if ! confirm "Proceed with Netbird configuration?" "y"; then
            log_info "Netbird configuration skipped"
            return 0
        fi
    fi

    configure_netbird "${NETBIRD_KEY}" "${NETBIRD_URL:-}" || return 1

    if [[ "${VERBOSE:-false}" == "true" ]]; then
        display_netbird_info
    fi

    log_success "Netbird VPN configured"
    return 0
}

# Run Static IP phase
run_static_ip_phase() {
    print_phase_header "12" "Static IP Configuration"

    # Check if static IP was requested via CLI
    if [[ -z "${STATIC_IP:-}" ]]; then
        log_info "No static IP requested via CLI"

        if [[ "${NON_INTERACTIVE:-false}" != "true" ]]; then
            # Show comprehensive network config box
            display_network_config_box

            local choice=""
            prompt_input "Select option (1-3)" "1" choice

            case "$choice" in
                1)
                    # Keep DHCP + VPN recommendation
                    log_info "Keeping DHCP configuration (VPN recommended for communication)"
                    return 0
                    ;;
                2)
                    # Static IP - show warning and prompt
                    echo ""
                    echo -e "  ${YELLOW}┌─────────────────────────────────────────────────────────────┐${NC}"
                    echo -e "  ${YELLOW}│${NC}  ${RED}WARNING: SSH connection may be lost if IP changes!${NC}         ${YELLOW}│${NC}"
                    echo -e "  ${YELLOW}│${NC}                                                             ${YELLOW}│${NC}"
                    echo -e "  ${YELLOW}│${NC}  Make sure you have physical access to the device or know  ${YELLOW}│${NC}"
                    echo -e "  ${YELLOW}│${NC}  the new IP address before proceeding.                     ${YELLOW}│${NC}"
                    echo -e "  ${YELLOW}└─────────────────────────────────────────────────────────────┘${NC}"
                    echo ""

                    if ! confirm "Proceed with static IP configuration?" "n"; then
                        log_info "Static IP configuration cancelled"
                        return 0
                    fi

                    local iface
                    iface=$(detect_network_interface)
                    prompt_input "Enter static IP (CIDR format, e.g., 192.168.1.42/24)" "" STATIC_IP
                    prompt_input "Enter gateway IP" "" GATEWAY
                    ;;
                3|*)
                    log_info "Skipping network configuration"
                    return 0
                    ;;
            esac
        else
            return 0
        fi
    fi

    if [[ -z "${STATIC_IP:-}" ]]; then
        return 0
    fi

    # Validate CIDR format
    if ! validate_cidr "${STATIC_IP}"; then
        log_error "Invalid IP format: ${STATIC_IP} (use CIDR, e.g., 192.168.1.42/24)"
        return 1
    fi

    # Get gateway if not provided
    if [[ -z "${GATEWAY:-}" ]]; then
        # Try to auto-detect
        GATEWAY=$(ip route show default 2>/dev/null | awk '/default/ {print $3}' | head -1)
        if [[ -z "$GATEWAY" ]]; then
            log_error "Gateway required for static IP configuration"
            return 1
        fi
        log_info "Auto-detected gateway: $GATEWAY"
    fi

    print_section "Static IP Configuration"

    display_network_info

    local iface
    iface=$(detect_network_interface)

    echo ""
    echo -e "  ${YELLOW}WARNING: Network configuration changes may disconnect SSH.${NC}"
    echo -e "  ${INFO} New IP: ${STATIC_IP}"
    echo -e "  ${INFO} Gateway: ${GATEWAY}"
    echo ""

    if [[ "${NON_INTERACTIVE:-false}" != "true" ]]; then
        if ! confirm "Apply static IP configuration?" "n"; then
            log_info "Static IP configuration cancelled"
            return 0
        fi
    fi

    # Try dhcpcd first, then NetworkManager
    if [[ -f "${DHCPCD_CONF}" ]]; then
        configure_static_ip_dhcpcd "$iface" "${STATIC_IP}" "${GATEWAY}"
    elif command_exists nmcli; then
        configure_static_ip_nm "$iface" "${STATIC_IP}" "${GATEWAY}"
    else
        log_error "No supported network configuration method found"
        return 1
    fi

    log_success "Static IP configuration complete"
    return 0
}
