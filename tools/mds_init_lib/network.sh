    printf "${CYAN}│${NC}    IP Address: %-58s ${CYAN}│${NC}\n" "${ip:-not assigned} (${dhcp_status})"
    printf "${CYAN}│${NC}    Gateway:    %-58s ${CYAN}│${NC}\n" "${gateway:-not set}"
