# Detect primary network interface
detect_network_interface() {
    # Try to find the default route interface
    local iface
    iface=$(ip route show default 2>/dev/null | awk '/default/ {print $5}' | head -1)

    if [[ -n "$iface" ]]; then
        echo "$iface"
        return 0
    fi

    # Fallback: look for eth0 or wlan0
    for candidate in eth0 wlan0 enp0s3; do
        if ip link show "$candidate" &>/dev/null; then
            echo "$candidate"
            return 0
        fi
    done

    echo ""
    return 1
}

# Get current IP information
get_current_ip_info() {
    local iface="$1"

    if [[ -z "$iface" ]]; then
        iface=$(detect_network_interface)
    fi

    if [[ -z "$iface" ]]; then
        return 1
    fi

    local ip gateway
    ip=$(ip -4 addr show "$iface" 2>/dev/null | grep -oP 'inet \K[\d.]+/\d+' | head -1)
    gateway=$(ip route show default 2>/dev/null | awk '/default/ {print $3}' | head -1)

    echo "interface=$iface"
    echo "ip=$ip"
    echo "gateway=$gateway"
}

# Display current network information
display_network_info() {
    local iface
    iface=$(detect_network_interface)

    echo ""
    echo -e "  ${BOLD}Current Network Configuration:${NC}"
    echo -e "  ${DIM}$(printf '%.0s─' {1..50})${NC}"

    if [[ -z "$iface" ]]; then
        echo "  No active network interface detected"
        return 1
    fi

    local ip gateway
    ip=$(ip -4 addr show "$iface" 2>/dev/null | grep -oP 'inet \K[\d.]+/\d+' | head -1)
    gateway=$(ip route show default 2>/dev/null | awk '/default/ {print $3}' | head -1)

    echo "  Interface: $iface"
    echo "  IP:        ${ip:-not assigned}"
    echo "  Gateway:   ${gateway:-not set}"
    echo ""
}

