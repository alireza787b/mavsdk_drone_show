# Check if Netbird is installed
check_netbird_installed() {
    command_exists netbird
}

# Install Netbird
install_netbird() {
    log_step "Installing Netbird VPN..."

    if is_dry_run; then
        echo -e "  ${DIM}[DRY-RUN] Would install Netbird${NC}"
        return 0
    fi

    # Use official install script
    log_info "Downloading Netbird installer..."
    curl -fsSL https://pkgs.netbird.io/install.sh | sh || {
        log_error "Failed to install Netbird"
        return 1
    }

    log_success "Netbird installed"
    return 0
}

# Configure Netbird
configure_netbird() {
    local setup_key="$1"
    local management_url="${2:-}"

    log_step "Configuring Netbird..."

    if is_dry_run; then
        echo -e "  ${DIM}[DRY-RUN] Would configure Netbird with setup key${NC}"
        return 0
    fi

    local cmd="netbird up --setup-key ${setup_key}"

    if [[ -n "$management_url" ]]; then
        cmd="$cmd --management-url ${management_url}"
    fi

    # Run netbird up
    if eval "$cmd"; then
        log_success "Netbird configured and connected"

        # Show assigned IP
        local nb_ip
        nb_ip=$(netbird status 2>/dev/null | grep -oP 'IP: \K[\d.]+' | head -1)
        if [[ -n "$nb_ip" ]]; then
            log_info "Netbird IP: $nb_ip"
            state_set_value "netbird_ip" "$nb_ip"
        fi

        return 0
    fi

    log_error "Failed to configure Netbird"
    return 1
}

# Get Netbird status
get_netbird_status() {
    if ! check_netbird_installed; then
        echo "not_installed"
        return
    fi

    local status
    status=$(netbird status 2>/dev/null | grep -i "status:" | head -1 | awk '{print $2}')
    echo "${status:-unknown}"
}

