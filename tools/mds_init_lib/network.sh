# Configure static IP (using dhcpcd)
configure_static_ip_dhcpcd() {
    local iface="$1"
    local static_ip="$2"
    local gateway="$3"

    log_step "Configuring static IP via dhcpcd..."

    if [[ ! -f "${DHCPCD_CONF}" ]]; then
        log_warn "dhcpcd.conf not found - using NetworkManager instead"
        return 1
    fi

    if is_dry_run; then
        echo -e "  ${DIM}[DRY-RUN] Would configure: ${DHCPCD_CONF}${NC}"
        echo -e "  ${DIM}  Interface: $iface${NC}"
        echo -e "  ${DIM}  IP: $static_ip${NC}"
        echo -e "  ${DIM}  Gateway: $gateway${NC}"
        return 0
    fi

    # Backup config
    backup_file "${DHCPCD_CONF}"

    # Check if interface config already exists
    if grep -q "interface ${iface}" "${DHCPCD_CONF}" 2>/dev/null; then
        log_warn "Static IP already configured for ${iface} in dhcpcd.conf"
        log_info "Please manually edit ${DHCPCD_CONF} to update"
        return 0
    fi

    # Append static IP configuration
    cat >> "${DHCPCD_CONF}" << EOF

# MDS Static IP Configuration
# Generated by mds_node_init.sh on $(date '+%Y-%m-%d %H:%M:%S')
interface ${iface}
static ip_address=${static_ip}
static routers=${gateway}
static domain_name_servers=${gateway} 8.8.8.8 8.8.4.4
EOF

    log_success "Static IP configured in ${DHCPCD_CONF}"
    log_warn "Reboot required to apply static IP changes"
    return 0
}

# Configure static IP (using NetworkManager)
configure_static_ip_nm() {
    local iface="$1"
    local static_ip="$2"
    local gateway="$3"

    log_step "Configuring static IP via NetworkManager..."

    if ! command_exists nmcli; then
        log_warn "NetworkManager not available"
        return 1
    fi

    if is_dry_run; then
        echo -e "  ${DIM}[DRY-RUN] Would configure static IP via nmcli${NC}"
        return 0
    fi

    # Get connection name
    local conn_name
    conn_name=$(nmcli -t -f NAME,DEVICE connection show --active | grep ":${iface}$" | cut -d: -f1)

    if [[ -z "$conn_name" ]]; then
        log_warn "No active connection found for ${iface}"
        return 1
    fi

    # Configure static IP
    nmcli connection modify "$conn_name" ipv4.method manual ipv4.addresses "$static_ip" ipv4.gateway "$gateway" ipv4.dns "8.8.8.8 8.8.4.4"

    log_success "Static IP configured for connection: $conn_name"
    log_warn "Run 'nmcli connection down/up \"$conn_name\"' to apply changes"
    return 0
}

