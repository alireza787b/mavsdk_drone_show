readonly TIMESYNCD_CONF="/etc/systemd/timesyncd.conf"
readonly DHCPCD_CONF="/etc/dhcpcd.conf"

# NTP servers in priority order
readonly NTP_SERVERS=(
    "0.pool.ntp.org"
    "1.pool.ntp.org"
    "2.pool.ntp.org"
    "3.pool.ntp.org"
)

readonly FALLBACK_NTP="ntp.ubuntu.com"

# =============================================================================
# NTP CONFIGURATION
# =============================================================================

# Configure NTP servers
configure_ntp() {
    log_step "Configuring NTP time synchronization..."

    if is_dry_run; then
        echo -e "  ${DIM}[DRY-RUN] Would configure NTP servers${NC}"
        return 0
    fi

    # Backup existing config
    if [[ -f "${TIMESYNCD_CONF}" ]]; then
        backup_file "${TIMESYNCD_CONF}"
    fi

    # Check if [Time] section exists
    if grep -q "^\[Time\]" "${TIMESYNCD_CONF}" 2>/dev/null; then
        # Update existing NTP line
        sed -i "s/^#*NTP=.*/NTP=${NTP_SERVERS[*]}/" "${TIMESYNCD_CONF}"
        sed -i "s/^#*FallbackNTP=.*/FallbackNTP=${FALLBACK_NTP}/" "${TIMESYNCD_CONF}"
    else
        # Create new config
        cat > "${TIMESYNCD_CONF}" << EOF
# MDS NTP Configuration
# Generated by mds_node_init.sh

[Time]
NTP=${NTP_SERVERS[*]}
FallbackNTP=${FALLBACK_NTP}
EOF
    fi

    chmod 644 "${TIMESYNCD_CONF}"
    log_success "NTP servers configured"
    return 0
}

# Enable NTP synchronization
enable_ntp_sync() {
    log_step "Enabling NTP synchronization..."

    if is_dry_run; then
        echo -e "  ${DIM}[DRY-RUN] Would enable NTP sync${NC}"
        return 0
    fi

    # Enable time sync
    timedatectl set-ntp true 2>/dev/null || {
        log_warn "timedatectl set-ntp failed, trying alternative method"
        systemctl enable systemd-timesyncd 2>/dev/null || true
        systemctl start systemd-timesyncd 2>/dev/null || true
    }

    log_success "NTP synchronization enabled"
    return 0
}

# Verify time synchronization
verify_time_sync() {
    log_step "Verifying time synchronization..."

    if is_dry_run; then
        echo -e "  ${DIM}[DRY-RUN] Would verify NTP sync${NC}"
        return 0
    fi

    # Check timedatectl status
    local sync_status
    sync_status=$(timedatectl show --property=NTPSynchronized --value 2>/dev/null || echo "no")

    if [[ "$sync_status" == "yes" ]]; then
        log_success "System time is synchronized"
        return 0
    fi

    # Not yet synced - may take a moment
    log_info "Time sync in progress (may take a few seconds)"

    # Wait briefly and check again
    sleep 3
    sync_status=$(timedatectl show --property=NTPSynchronized --value 2>/dev/null || echo "no")

    if [[ "$sync_status" == "yes" ]]; then
        log_success "System time is synchronized"
        return 0
    fi

    log_warn "Time sync not confirmed yet (will sync automatically)"
    return 0
}

# Display time info
display_time_info() {
    echo ""
    echo -e "  ${BOLD}Time Configuration:${NC}"
    echo -e "  ${DIM}$(printf '%.0s─' {1..50})${NC}"

    local current_time timezone ntp_status
    current_time=$(date '+%Y-%m-%d %H:%M:%S %Z')
    timezone=$(timedatectl show --property=Timezone --value 2>/dev/null || echo "Unknown")
    ntp_status=$(timedatectl show --property=NTPSynchronized --value 2>/dev/null || echo "Unknown")

    echo "  Current time: $current_time"
    echo "  Timezone:     $timezone"
    echo "  NTP synced:   $ntp_status"
    echo ""
}

